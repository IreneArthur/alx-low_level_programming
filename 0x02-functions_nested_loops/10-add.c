#include <stdio.h>
#include "main.h"
/**
 * add - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Description: This function takes two integers, 'a' and 'b', as input
 *              parameters and returns their sum. It simply adds the values
 *              of 'a' and 'b' together and returns the result.
 *
 * Return: The sum of the integers 'a' and 'b'.
 */
int add(int a, int b)
{
	return (a + b);
}
